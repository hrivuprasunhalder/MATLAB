age = input("Age : ")

if age > 60
    disp("Senior Citizen");

elseif age > 21 && age <= 60
    disp("Working");

elseif age > 17 && age <= 21
    disp("college");

elseif age > 6 && age <= 17
    disp("School");

elseif age >= 1 && age <= 6
    disp("home");    

elseif age == 0
    disp("just born");

else
    disp("he is in womb or in testicles i.e invalid");

end    